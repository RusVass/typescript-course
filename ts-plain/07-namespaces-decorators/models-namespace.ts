namespace Models {
    export interface IUser {
        id: number;
        name: string;
    }

    export interface IProduct {
        id: number;
        name: string;
        price: number;
    }
}

/* 
лучше использовать ES-модули (import/export):

export interface IUser {
    id: number;
    name: string;
}
export interface IProduct {
    id: number;
    name: string;
    price: number;
}
*/


/* 
Основные различия и преимущества ES-модулей по сравнению с пространствами имен:

1. Область видимости:
  - Пространства имен (namespace): Код внутри пространства имен технически все еще находится в глобальной области видимости, но доступен через идентификатор пространства имен. Это может привести к конфликтам имен, если пространства имен не управляются тщательно, особенно в крупных проектах.
  - ES-модули: Каждый модуль имеет свою собственную частную область видимости. Переменные, функции и классы, определенные в модуле, недоступны извне, если они явно не экспортированы. Это обеспечивает истинную инкапсуляцию и избегает конфликтов имен.

2. Загрузка файлов и управление зависимостями:
  - Пространства имен: Обычно требуют использования пользовательских загрузчиков модулей или инструментов для сборки, чтобы управлять зависимостями между файлами в пространствах имен.
  - ES-модули: Используют стандартные операторы `import` и `export`, которые понимаются современными JavaScript-окружениями и сборщиками. Это упрощает управление зависимостями и позволяет использовать такие функции, как tree-shaking (удаление неиспользуемого кода).

3. Ленивое (отложенное) выполнение:
  - Пространства имен: Обычно весь код внутри пространства имен загружается сразу, даже если используется только его небольшая часть.
  - ES-модули: Поддерживают динамические импорты, позволяя загружать модули асинхронно и только по мере необходимости. Это может улучшить время начальной загрузки и производительность.

4. Стандартизация:
  - Пространства имен: Особенность, специфичная для TypeScript.
  - ES-модули: Часть стандарта JavaScript, обеспечивающая лучшую совместимость и интероперабельность с другим JavaScript-кодом и инструментами.
*/

// Рекомендация: Для новых проектов и при рефакторинге старого кода всегда предпочитайте ES-модули пространствам имен. ES-модули предоставляют более надежный, стандартизированный и поддерживаемый способ организации вашего TypeScript-кода.
